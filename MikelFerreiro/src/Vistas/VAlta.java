    private void bPlusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPlusActionPerformed
        try{
        for (int x=0;x<asignaturasAlumno.size();x++){
            if(asignaturasAlumno.get(x).getNombre().equals(cbAsignaturas.getSelectedItem())){
                throw new Excepcion(4);
            }
        }
        Asignatura a=new Asignatura();
        a.setNombre(String.valueOf(cbAsignaturas.getSelectedItem()));
        asignaturasAlumno.add(a);
        JOptionPane.showMessageDialog(this,"Asignatura aÃ±adida");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_bPlusActionPerformed

    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try{
        if(tfNombre.getText().isEmpty())
            throw new Excepcion(6);
        if(tfDni.getText().isEmpty())
            throw new Excepcion(5);
        if(!tfDni.getText().matches("^[0-9]{8}[A-Z]{1}$"))
             throw new Excepcion(10);
        if(asignaturasAlumno.isEmpty())
            throw new Excepcion(7);
        if(Main.altaAlumno(tfDni.getText(),tfNombre.getText(),asignaturasAlumno)){
            JOptionPane.showMessageDialog(this,"Alumno dado de alta correctamente");
            Main.reabrirVentana(this);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_bAceptarActionPerformed

