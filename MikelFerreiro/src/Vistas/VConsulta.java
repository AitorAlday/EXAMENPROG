    private void rellenar1(ArrayList<Alumno> alumnos) {
        String texto="";
        for (int x=0;x<alumnos.size();x++){
            texto+=alumnos.get(x).getDni()+"   "+alumnos.get(x).getNombre()+"\n";
        }
        ta.setText(texto);
    }
    
    private void rellenar2(ArrayList<Asignatura> asignaturas) {
        String texto="";
        for (int x=0;x<asignaturas.size();x++){
            texto+=asignaturas.get(x).getNombre()+"\n";
        }
        ta.setText(texto);
    }
