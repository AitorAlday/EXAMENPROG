    private void bConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConsultarActionPerformed
        try{
        if(rbAsignatura.isSelected() || rbAlumno.isSelected()){
            if(tfNombre.getText().isEmpty())
                throw new Excepcion(5);
            if(rbAsignatura.isSelected()){
                ArrayList<Alumno> alumnos=Main.buscarPorAsignatura(tfNombre.getText());
                if(alumnos.size()>0){
                rellenar1(alumnos);
                }else{
                    tfNombre.setText("");
                    throw new Excepcion(9);
                }
            }else{
                ArrayList<Asignatura> asignaturas=Main.buscarPorAlumno(tfNombre.getText());
                if(asignaturas.size()>0){
                rellenar2(asignaturas);
                }else{
                    tfNombre.setText("");
                    throw new Excepcion(9);
                }
            }
        }else{
            throw new Excepcion (8);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_bConsultarActionPerformed

